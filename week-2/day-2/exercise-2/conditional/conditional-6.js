// branching bertumpuk (switch case)

const counterButton = 1;

switch(counterButton) {
    case 1 : {
        console.log("mengarah kedepan");
        break;
    }
    case 2 : {
        console.log("mengarah kekanan");
        break;
    }
    case 3 : {
        console.log("mengarah kebelakang");
        break;
    }
    case 4 : {
        console.log("mengarah kekiri");
        break;
    }
    case 5 : {
        console.log("Terbang cok");
        break;
    }
    default : {
        console.log("diem aja lah ya. mager bat asli");
    }
}